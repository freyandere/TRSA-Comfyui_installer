class SimpleInstaller:
    """–ü—Ä–æ—Å—Ç–æ–π —É—Å—Ç–∞–Ω–æ–≤—â–∏–∫ ComfyUI Accelerator —Å –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–Ω—ã–º–∏ –æ—à–∏–±–∫–∞–º–∏"""
    
    def __init__(self):
        self.python_exe = self._find_python()
        self.language = self._detect_language_modern()
        self.messages = self._load_messages()
        
        # –ü–æ–¥–∞–≤–ª—è–µ–º DeprecationWarning –¥–ª—è —á–∏—Å—Ç–æ–≥–æ –≤—ã–≤–æ–¥–∞
        warnings.filterwarnings("ignore", category=DeprecationWarning)
        
    def _find_python(self) -> str:
        """–ü–æ–∏—Å–∫ –∏—Å–ø–æ–ª–Ω—è–µ–º–æ–≥–æ —Ñ–∞–π–ª–∞ Python"""
        for exe in ["python.exe", "python"]:
            if os.path.exists(exe):
                return exe
        raise FileNotFoundError("Python executable not found")
    
    def _detect_language_modern(self) -> str:
        """
        –°–æ–≤—Ä–µ–º–µ–Ω–Ω—ã–π —Å–ø–æ—Å–æ–± –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è —è–∑—ã–∫–∞ (–∏—Å–ø—Ä–∞–≤–ª–µ–Ω–æ DeprecationWarning)
        –ó–∞–º–µ–Ω—è–µ—Ç —É—Å—Ç–∞—Ä–µ–≤—à–∏–π locale.getdefaultlocale()
        """
        try:
            # –ú–µ—Ç–æ–¥ 1: –ü–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è (–Ω–∞–∏–±–æ–ª–µ–µ –Ω–∞–¥–µ–∂–Ω—ã–π)
            for env_var in ['LC_ALL', 'LC_CTYPE', 'LANG', 'LANGUAGE']:
                lang = os.environ.get(env_var, '').lower()
                if lang.startswith('ru'):
                    return 'ru'
            
            # –ú–µ—Ç–æ–¥ 2: Windows - –ø—Ä–æ–≤–µ—Ä—è–µ–º —Ä–µ–µ—Å—Ç—Ä
            if os.name == 'nt':
                try:
                    import subprocess
                    result = subprocess.run([
                        'reg', 'query', 'HKCU\\Control Panel\\International',
                        '/v', 'LocaleName'
                    ], capture_output=True, text=True, timeout=5)
                    
                    if result.returncode == 0:
                        for line in result.stdout.split('\n'):
                            if 'LocaleName' in line and 'ru' in line.lower():
                                return 'ru'
                except:
                    pass
            
            # –ú–µ—Ç–æ–¥ 3: –°–æ–≤—Ä–µ–º–µ–Ω–Ω—ã–π locale API (Python 3.11+)
            try:
                import locale
                # –ò—Å–ø–æ–ª—å–∑—É–µ–º —Å–æ–≤—Ä–µ–º–µ–Ω–Ω—ã–π API –≤–º–µ—Å—Ç–æ —É—Å—Ç–∞—Ä–µ–≤—à–µ–≥–æ getdefaultlocale()
                current_locale = locale.getlocale()
                if current_locale[0] and current_locale[0].lower().startswith('ru'):
                    return 'ru'
                    
                # –ê–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–Ω—ã–π —Å–ø–æ—Å–æ–± —á–µ—Ä–µ–∑ encoding
                encoding = locale.getencoding()
                if 'ru' in encoding.lower():
                    return 'ru'
            except:
                pass
                
        except Exception:
            pass
        
        # Fallback - –∞–Ω–≥–ª–∏–π—Å–∫–∏–π –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é
        return 'en'
    
